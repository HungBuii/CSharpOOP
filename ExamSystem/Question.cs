class Question
{
    // Constructor
    public Question() { }

    // Properties
    private int no;
    public int No
    {
        get { return no; }
        set { no = value; }
    }
    
    private string title;
    public string Title
    {
        get { return title; }
        set { title = value; }
    }
    
    private string answerChoice;
    public string AnswerChoice
    {
        get { return answerChoice; }
        set { answerChoice = value; }
    }
    
    // Method    
}